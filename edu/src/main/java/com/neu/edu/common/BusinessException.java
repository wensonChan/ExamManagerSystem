public class BusinessException extends Exception {

    private String msg;

    public BusinessException() {
        super();
    }

    public BusinessException(String msg) {
        this.msg = msg;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
